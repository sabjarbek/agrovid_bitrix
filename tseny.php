<h3>Покупка на вторичном рынке</h3>
<p>
	 Квартиру вторичного рынка приобрести немного сложнее, чем в новостройке. Поэтому комиссия риэлтора может быт увеличена и составляет примерно 2-3%. Агенту требуется проверка жилья на юридическую чистоту, наличие обременений, долгов и многого другого.
</p>
<h3>Ипотека</h3>
<p>
	 В таком случае агенту необходимо провести переговоры с банком, подготовить полный пакет документов и помочь клиенту в оформлении недвижимости. Стоимость услуг варьируется от 2 до 5% от цены самого жилья. В случае каких-либо сложностей, например, плохой кредитной истории клиента, гонорар возрастает до 12%. <br>
	 Размер оплаты определяется риэлтором и она может быть как фиксированной суммой, так и процентной. Например, многие московские агентства взимают гонорары от 2%, при этом не меньше 100 000 рублей. Сумма уменьшается вдвое, если покупатель самостоятельно нашел продавца. На данной странице вы можете подробнее ознакомиться с прайсом на риэлторские услуги.
