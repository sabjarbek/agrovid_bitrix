<h3>Ценообразование</h3>
<p>
	 На вопрос “сколько стоят услуги частного риэлтора” нет однозначного ответа. Обычно за работу агенты берут определенный процент. В среднем он составляет 2,5% от стоимости объекта недвижимости. Максимальная ставка - 6%. Стоит учитывать, что комиссия при покупке жилья на порядок выше, чем при продаже, поскольку в первом случае специалисту требуется приложить больше времени и усилий. <br>
	 Также существуют услуги риэлтора, расценки на которые могут быть смешанными. В таком случае при продаже агент получает процент, а дополнительную помощь оказывает уже за фиксированную сумму. Все детали необходимо обговорить еще до заключения договора.
</p>
